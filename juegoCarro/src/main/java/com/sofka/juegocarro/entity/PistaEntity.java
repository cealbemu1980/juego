package com.sofka.juegocarro.entity;

/**
 *
 * @author cealb
 */
public class PistaEntity {
    private int idpista;
    private int distancia;
    private int numerocarriles;
    
    /**
     * @return the idpista
     */
    public int getIdpista() {
        return idpista;
    }

    /**
     * @param idpista the idpista to set
     */
    public void setIdpista(int idpista) {
        this.idpista = idpista;
    }


    /**
     * @return the distancia
     */
    public int getDistancia() {
        return distancia;
    }

    /**
     * @param distancia the distancia to set
     */
    public void setDistancia(int distancia) {
        this.distancia = distancia;
    }

    /**
     * @return the numerocarriles
     */
    public int getNumerocarriles() {
        return numerocarriles;
    }

    /**
     * @param numerocarriles the numerocarriles to set
     */
    public void setNumerocarriles(int numerocarriles) {
        this.numerocarriles = numerocarriles;
    }
    
    
}
